package ru.job4j.math;

public class   MathFunction {
    public static double sum(double first, double second) {
        return first + second;
    }

    public static double ultiply(double first, double second) {

        return first * second;
    }

    public static double rust(double first, double second) {
        return first - second;
    }

    public static double misha(double first, double second) {
        return first / second;
    }
}


